a = input('masukan bilangan pertama : ')
b = input('masukan bilangan kedua : ')
c = input('masukan bilangan kedua : ')
num1 = int(a)
num2 = int(b)
num3 = int(c)
print(num1, '+' , num2, '+' , num3 , '=' , num1 + num2 + num3)
print(num1, '-' , num2, '-' , num3 , '=' , num1 - num2 - num3)
print(num1, '*' , num2, '*' , num3 , '=' , num1 * num2 * num3)
print(num1, '//' , num2, '//' , num3 , '=' , num1 // num2 // num3)